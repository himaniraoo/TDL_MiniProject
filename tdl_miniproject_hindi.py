    print(f"Best Exp 1 checkpoint: {exp1_best_ckpt}")

    # Free Exp 1 model from GPU memory before loading Exp 2
    del model_exp1, trainer_exp1
    torch.cuda.empty_cache()

    # ─────────────────────────────────────────────────────────────────────────
    # EXPERIMENT 2 — Warm-Start from Exp 1 Checkpoint, Fine-tune on Hindi
    # ─────────────────────────────────────────────────────────────────────────
    # We load the BEST checkpoint saved during Exp 1 (English-trained weights).
    # This is the key design choice: Exp 2 continues cross-lingual adaptation
