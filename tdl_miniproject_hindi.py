    print(f"  SUMMARY — {MODEL_NAME}")
    print(f"{'='*60}")
    print(f"  Fertility:     {fertility_score:.4f}")
    print(f"  Zero-Shot F1:  {results_exp1['eval_f1']:.4f}  (English-trained → Hindi)")
    print(f"  Fine-Tuned F1: {results_exp2['eval_f1']:.4f}  (warm-start → Hindi adapted)")
