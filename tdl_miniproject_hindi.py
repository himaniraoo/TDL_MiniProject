def compute_fertility(tokenizer, dataset):
    """
    Token fertility = average subword tokens per Hindi word.
    1.0 = model knows every Hindi word perfectly.
    Higher = model fragments Hindi words more = weaker Hindi understanding.
    """
    total_words = total_subwords = 0
    for example in dataset:
        words           = example["tokens"]
        total_words    += len(words)
        total_subwords += len(
            tokenizer(words, is_split_into_words=True,
                      add_special_tokens=False)["input_ids"]
        )
    return total_subwords / total_words


def tokenize_and_align_labels(examples, tokenizer, label_all_tokens=False):
    """
    Aligns NER labels (per word) to subword tokens:
      - First subword of each word → real label
      - Continuation subwords      → -100 (ignored by loss and eval)
      - Special tokens [CLS]/[SEP] → -100
    """
    tokenized = tokenizer(
        examples["tokens"],
        truncation=True,
        max_length=MAX_LEN,
        is_split_into_words=True,
    )
    aligned_labels = []
    for i, label_seq in enumerate(examples["ner_tags"]):
        word_ids     = tokenized.word_ids(batch_index=i)
        prev_word_id = None
        label_ids    = []
        for word_id in word_ids:
            if word_id is None:
                label_ids.append(-100)
            elif word_id != prev_word_id:
                label_ids.append(label_seq[word_id])
            else:
                label_ids.append(label_seq[word_id] if label_all_tokens else -100)
            prev_word_id = word_id
        aligned_labels.append(label_ids)
    tokenized["labels"] = aligned_labels
    return tokenized


def make_compute_metrics(label_names):
    """
    seqeval-based metrics. Scores at entity span level (entire span must be correct).
    Also returns per-entity F1 for PER, ORG, LOC.
    """
    def compute_metrics(eval_pred):
        logits, labels = eval_pred
        predictions    = np.argmax(logits, axis=-1)
        true_labels = [
            [label_names[l] for l in row if l != -100]
            for row in labels
        ]
        true_preds = [
            [label_names[p] for p, l in zip(pred_row, label_row) if l != -100]
            for pred_row, label_row in zip(predictions, labels)
        ]
        report  = classification_report(true_labels, true_preds, output_dict=True)
        metrics = {
            "f1":        report["micro avg"]["f1-score"],
            "precision": report["micro avg"]["precision"],
            "recall":    report["micro avg"]["recall"],
        }
        for entity in ["PER", "ORG", "LOC"]:
            if entity in report:
                metrics[f"f1_{entity}"] = report[entity]["f1-score"]
        return metrics
    return compute_metrics


def make_trainer(model, args, train_ds, eval_ds, tokenizer):
    return Trainer(
        model=model,
        args=args,
        train_dataset=train_ds,
        eval_dataset=eval_ds,
        processing_class=tokenizer,
        data_collator=DataCollatorForTokenClassification(tokenizer),
        compute_metrics=make_compute_metrics(label_names),
    )


def clear_checkpoints(run_name):
    for prefix in ["exp1_", "exp2_"]:
        d = f"./{prefix}{run_name}"
        if os.path.exists(d):
            shutil.rmtree(d)

# ── CELL 6 — MAIN LOOP: Run All 6 Models ──────────────────────────────────────
# Experimental design:
#
#   Exp 1 — Cross-lingual zero-shot transfer
#            Fresh pretrained model → train on English WikiANN → zero-shot test on Hindi
#            Measures: how much NER knowledge transfers across languages without Hindi data
#
#   Exp 2 — Cross-lingual warm-start adaptation
#            Loads BEST CHECKPOINT from Exp 1 (English-trained weights) → fine-tune on Hindi
#            Measures: how much English NER warm-starting helps Hindi adaptation
#
#   Gap    = fine_f1 - zero_f1
