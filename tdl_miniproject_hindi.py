    # ── Tokenize datasets (fresh for each model — tokenizer changes) ──────────
    def align(examples):
        return tokenize_and_align_labels(examples, tokenizer)

    conll_tokenized = conll.map(align, batched=True, remove_columns=conll["train"].column_names)
    hiner_tokenized = hiner.map(align, batched=True, remove_columns=hiner["train"].column_names)
    hiner_test_tok  = hiner["test"].map(align, batched=True, remove_columns=hiner["test"].column_names)

    # ─────────────────────────────────────────────────────────────────────────
    # EXPERIMENT 1 — Train on English, Zero-Shot Test on Hindi
    # ─────────────────────────────────────────────────────────────────────────
